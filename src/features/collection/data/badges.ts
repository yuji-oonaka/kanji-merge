// src/features/collection/data/badges.ts

export interface Badge {
  id: string;
  char: string;
  isMessage: boolean;
}

export const STAGES_PER_BADGE = 10;

// 1周目: "あそんでくれてありがとう" (変更なし)
const FIRST_LOOP_CHARS = [
  "あ", "歩", "空", "静", "思", "考", "巡", "旅", "道", "知",
  "そ", "理", "解", "ん", "結", "束", "で", "水", "流", "時",
  "く", "雲", "光", "れ", "歴", "史", "て", "手", "触", "感",
  "あ", "り", "在", "が", "画", "と", "都", "う", "有", "終"
];

// 2周目: "おなじときをいきてくれて"
// 漢字とひらがなの「ズレ」と「意味の重なり」を楽しむ構成
const SECOND_LOOP_CHARS = [
  // 41-50: [同] [じ] ... [時/刻] [を]
  "同", "じ", "空", "間", "違", "う", "場", "所", "で", "も",
  // 51-60: [時] ... [生] [き] [て]
  "時", "刻", "を", "重", "ね", "今", "を", "生", "き", "て",
  // 61-70: [く] [れ] [て]
  "選", "ん", "で", "く", "れ", "て", "傍", "に", "居", "て",
  // 71-80: 余韻 (響き、凪、永遠)
  "響", "き", "合", "う", "鼓", "動", "と", "永", "遠", "凪"
];

const ALL_CHARS = [...FIRST_LOOP_CHARS, ...SECOND_LOOP_CHARS];

export const BADGES: Badge[] = ALL_CHARS.map((char, index) => ({
  id: `badge_${index}`,
  char,
  // メッセージ判定用のひらがなチェック
  // 2周目は漢字混じりで読むため、システム的な「isMessage」フラグはあまり重要ではなくなるが、
  // 演出（色変え等）のために残しておく、あるいは2周目はあえて色を変えないという選択も可。
  // ここでは「ひらがな」だけ色を変える（ヒントにする）従来のロジックを維持。
  isMessage: /^[ぁ-ん]+$/.test(char),
}));