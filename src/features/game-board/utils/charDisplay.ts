/**
 * データ上の特殊記号や異体字を、ユーザーが納得できる一般的な文字に変換して表示する。
 */
export function getDisplayChar(char: string): string {
  const charMap: Record<string, string> = {
    // --- データ生成エラー記号の救済 ---
    "③": "才", // 在、存
    "④": "小", // 赤
    "⑤": "白", // 既、即
    "⑥": "曲", // 典
    "⑦": "鳥", // 島

    // --- 異体字・特殊文字の「新字体」への置き換え ---
    "𢚩": "急", // 穏、隠の右側（★ここを追加）
    "𤴓": "疋", // 定、是の下部
    "𠂒": "牛", // 先、告の上部
    "冗": "売", // 売の上部
    "𠃜": "眉", // 声の上部
    "𠮷": "吉", // 舎などの一部
    "𫩠": "堂", // 常、賞の上部
    "𠂉": "丿", 
    "𠩵": "暦", 
    "𭕄": "巣", 
    "𦍌": "羊", 
    "𡗗": "春", 
    "𠦝": "幹", 
    "𫢉": "干", 
    "𭷔": "牛", // 解の右下
    "𭔰": "豆", // 闘の中身
    
    // その他表示崩れしやすい部首
    "⺦": "爿",
    "⺮": "竹",
    "⺘": "扌",
    "⺡": "氵",
    "⺨": "犭",
    "⺣": "灬",
    "⺖": "忄",
    "⺗": "心",
    "⺹": "耂",
    "⺺": "言",
    "⻌": "辶",
    "⻏": "阝",
    "⻖": "阝",
  };

  return charMap[char] || char;
}